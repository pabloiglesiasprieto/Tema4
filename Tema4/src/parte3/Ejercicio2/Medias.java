package parte3.Ejercicio2;

public class Medias {
	// Creamos la funcion media con 2 parámetros.
	public static double media(int a, int b) {
		double media = (a + b) / 2;
		return media;
	}

	// Creamos la funcion media con 3 parámetros.
	public static double media(int a, int b, int c) {
		double media = (a + b + c) / 3;
		return media;
	}
}
